def solve_mixture(target_name, source_names):
    target_coords = np.array(data[target_name])
    source_matrix = np.array([data[name] for name in source_names]).T
    
    # Use NNLS (Non-Negative Least Squares) to find weights
    weights, residual = nnls(source_matrix, target_coords)
    
    # Normalize weights to sum to 100%
    total_weight = np.sum(weights)
    if total_weight > 0:
        normalized_weights = weights / total_weight * 100
    else:
        normalized_weights = weights
        
    return normalized_weights, residual

